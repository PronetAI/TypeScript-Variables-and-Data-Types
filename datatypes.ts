// Built-in data types | User Defined Data Types
// number     |    array
// string     |    class
// boolean    |    interface
// undefined  |    tuple
// void       |    enum
// null       |    function
// never      |    ----

// number: alows all integers
var variable : number = 100;

// string: allows all strings
var variable : string = "Hello World";

// boolean: boolean is true or false
var variable : boolean = true;

// void
var variable : void = undefined ;

// null: nothing stored in variable
var variable = null;

// undefined: unknown 
var variable = undefined;

// never
var variable : never = null;

// any: any data types
var variable : any;
